#include "E02H_distance_pattern_strings.h"

int DistanceBetweenPatternsStrings(vector<string> dna, const string &pattern) {
    int k = pattern.size();
    int distance = 0;

    for (auto string : dna) {
        int h_distance = 10000000;

        for (int i = 0; i < string.size() - k; i++) {
            auto n_pattern = Text(string, i, k);
            auto n_distance = HammingDistance(pattern, n_pattern);

            h_distance = h_distance > n_distance ? n_distance : h_distance;
        }

        distance += h_distance;
    }

    return distance;
}

